-- Creating tables --
CREATE TABLE "USER"
(
  "uid"    Int,
  username CHAR(40),
  PRIMARY KEY ("uid")
);

CREATE TABLE MAKECOMTITLE
(
  cid   Int,
  title CHAR(25) DEFAULT 'Comparison',
  PRIMARY KEY (cid)
);

CREATE TABLE MAKECOMPRIV
(
  cid       Int,
  "uid"     Int,
  privilege CHAR(40),
  PRIMARY KEY (cid),
  FOREIGN KEY ("uid") REFERENCES "USER" ("uid")
);
CREATE TABLE SPECSOS
(
  processor    CHAR(35),
  os           CHAR(40),
  chargingPort CHAR(40),
  PRIMARY KEY (processor)
);

CREATE TABLE SPECSDISPLAY
(
  length      FLOAT,
  width       FLOAT,
  displayType CHAR(40),
  displaySize Float,
  PRIMARY KEY (length, width, displayType)
);

CREATE TABLE PHONEWEIGHT
(
  model       CHAR(40),
  releaseDate DATE,
  weight      INT,
  PRIMARY KEY (model)
);

CREATE TABLE PHONEPRICE
(
  model       CHAR(40),
  storageSize Int,
  memory      Int,
  price       Int,
  PRIMARY KEY (model)
);

CREATE TABLE SPECSPROCESSOR
(
  length      FLOAT,
  width       FLOAT,
  displayType CHAR(40),
  processor   CHAR(35),
  model       CHAR(40),
  PRIMARY KEY (length, width, displayType),
  FOREIGN KEY (processor) REFERENCES SPECSOS (processor),
  FOREIGN KEY (model) REFERENCES PHONEPRICE (model),
  FOREIGN KEY (length, width, displayType) REFERENCES SPECSDISPLAY (length, width, displayType)
);


CREATE TABLE "BETWEEN"
(
  cid   Int,
  model CHAR(40),
  PRIMARY KEY (cid, model),
  FOREIGN KEY (cid)
    REFERENCES MAKECOMPRIV (cid)
      ON DELETE CASCADE,
  FOREIGN KEY (model)
    REFERENCES PHONEPRICE (model)
);

CREATE TABLE MANUFACTURER
(
  companyName CHAR(40),
  address     CHAR(50),
  PRIMARY KEY (companyName)
);

CREATE TABLE PRODUCESCOMPANY
(
  model       CHAR(40),
  companyName CHAR(40),
  PRIMARY KEY (model),
  FOREIGN KEY (model) REFERENCES PHONEPRICE (model),
  FOREIGN KEY (companyName) REFERENCES MANUFACTURER (companyName)
);

CREATE TABLE PRODUCESPRICE
(
  model CHAR(40),
  price FLOAT,
  PRIMARY KEY (model, price),
  FOREIGN KEY (model) REFERENCES PHONEPRICE (model)
);

CREATE TABLE COLOUR
(
  colourType CHAR(40),
  PRIMARY KEY (colourType)
);
CREATE TABLE COMESIN
(
  colourType CHAR(40),
  model      CHAR(40),
  PRIMARY KEY (colourType, model),
  FOREIGN KEY (colourType) REFERENCES COLOUR (colourType),
  FOREIGN KEY (model) REFERENCES PHONEPRICE (model)
);
CREATE TABLE ACCESSORY
(
  model CHAR(40),
  PRIMARY KEY (model)
);
CREATE TABLE COMESWITH
(
  phoneModel CHAR(40),
  accModel   CHAR(40),
  PRIMARY KEY (phoneModel, accModel),
  FOREIGN KEY (phoneModel) REFERENCES PHONEPRICE (model),
  FOREIGN KEY (accModel) REFERENCES ACCESSORY (model)
);
CREATE TABLE HEADPHONES
(
  model CHAR(40),
  type  CHAR(40),
  PRIMARY KEY (model),
  FOREIGN KEY (model) REFERENCES ACCESSORY (model)
);
CREATE TABLE CHARGER
(
  model   CHAR(40),
  voltage INT,
  PRIMARY KEY (model),
  FOREIGN KEY (model) REFERENCES ACCESSORY (model)
);
CREATE TABLE "CASE"
(
  model      CHAR(40),
  waterProof CHAR(10),
  PRIMARY KEY (model),
  FOREIGN KEY (model) REFERENCES ACCESSORY (model)
);

CREATE TABLE CAMERA
(
  type        CHAR(40),
  cameraModel CHAR(40),
  mp          INT,
  phoneModel  CHAR(40),
  PRIMARY KEY (phoneModel, type, cameraModel),
  FOREIGN KEY (phoneModel) REFERENCES PHONEPRICE (model)
);

